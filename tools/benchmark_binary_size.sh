#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
LOG_ROOT="${ROOT_DIR}/local_logs/size_bench"
RUNS_DIR="${LOG_ROOT}/runs"
HISTORY_CSV="${LOG_ROOT}/history.csv"

PRELOAD_FILE="${ROOT_DIR}/local_deps/croft-deps.cmake"
DEBUG_BUILD_DIR="build-size-debug"
OPT_BUILD_DIR="build-size-opt"
KEEP_LOGS="${CROFT_SIZE_BENCH_KEEP:-80}"
OPT_LEVEL="${CROFT_SIZE_BENCH_OPT_LEVEL:-Os}"
COMPARE_DEBUG=0

TARGETS=()

usage() {
    cat <<'USAGE'
Usage:
  tools/benchmark_binary_size.sh [options]

Builds the Croft example set in a size-oriented release profile and records
the size of each produced example binary. By default the target list comes
from build/croft-example-targets.txt generated by CMake.

Options:
  --target <cmake-target>      Benchmark one target. May be repeated.
  --targets <a,b,c>            Benchmark a comma-separated list of targets.
  --preload <cmake-cache>      Preload dependency cache file
                               (default: local_deps/croft-deps.cmake if present)
  --debug-dir <dir>            Build dir for Debug profile (default: build-size-debug)
  --opt-dir <dir>              Build dir for optimized profile (default: build-size-opt)
  --keep <count>               Keep newest N run logs (default: 80)
  --opt-level <Os|Oz|O2|O3>    Compile optimization level for optimized profile (default: Os)
  --compare-debug              Also build and measure Debug binaries
  --help                       Show this help

Examples:
  tools/benchmark_binary_size.sh
  tools/benchmark_binary_size.sh --target example_editor_text
  tools/benchmark_binary_size.sh --targets example_foundation_threads,example_sapling_text
USAGE
}

sanitize() {
    printf '%s' "$1" | tr '/[:space:]' '__' | tr -cd '[:alnum:]_.-,'
}

bytes_of() {
    local file="$1"
    if stat -f%z "$file" >/dev/null 2>&1; then
        stat -f%z "$file"
    else
        wc -c < "$file" | tr -d '[:space:]'
    fi
}

cleanup_old_logs() {
    local keep="$1"
    if [[ ! "$keep" =~ ^[0-9]+$ ]]; then
        return
    fi

    shopt -s nullglob
    local all_logs=("${RUNS_DIR}"/*.log)
    shopt -u nullglob
    if (( ${#all_logs[@]} <= keep )); then
        return
    fi

    local sorted=()
    while IFS= read -r line; do
        sorted+=("$line")
    done < <(ls -1t "${RUNS_DIR}"/*.log)

    local i
    for ((i = keep; i < ${#sorted[@]}; ++i)); do
        rm -f "${sorted[i]}"
    done
}

print_cmd() {
    printf '$'
    local arg
    for arg in "$@"; do
        printf ' %q' "$arg"
    done
    printf '\n'
}

run_cmd() {
    print_cmd "$@"
    set +e
    "$@"
    local rc=$?
    set -e
    return "$rc"
}

append_history_header_if_missing() {
    if [[ -f "$HISTORY_CSV" ]]; then
        return
    fi

    cat >"$HISTORY_CSV" <<'CSV'
timestamp,git_describe,target,profile,build_dir,build_type,opt_level,lto,dead_strip,binary_relpath,binary_bytes,stripped_bytes,log_file
CSV
}

append_history_row() {
    local timestamp="$1"
    local git_desc="$2"
    local target="$3"
    local profile="$4"
    local build_dir="$5"
    local build_type="$6"
    local opt_level="$7"
    local lto="$8"
    local dead_strip="$9"
    local binary_relpath="${10}"
    local binary_bytes="${11}"
    local stripped_bytes="${12}"
    local log_file="${13}"

    printf '%s,%s,%s,%s,%s,%s,%s,%s,%s,%s,%s,%s,%s\n' \
        "$timestamp" \
        "$git_desc" \
        "$target" \
        "$profile" \
        "$build_dir" \
        "$build_type" \
        "$opt_level" \
        "$lto" \
        "$dead_strip" \
        "$binary_relpath" \
        "$binary_bytes" \
        "$stripped_bytes" \
        "$log_file" >>"$HISTORY_CSV"
}

append_target() {
    local target="$1"
    if [[ -z "$target" ]]; then
        return
    fi

    local existing
    for existing in "${TARGETS[@]:-}"; do
        if [[ "$existing" == "$target" ]]; then
            return
        fi
    done
    TARGETS+=("$target")
}

load_targets_from_manifest() {
    local build_dir="$1"
    local manifest="${build_dir}/croft-example-targets.txt"

    if [[ ! -f "$manifest" ]]; then
        echo "ERROR: example target manifest not found: ${manifest}" >&2
        return 1
    fi

    while IFS= read -r line; do
        [[ -n "$line" ]] || continue
        append_target "$line"
    done < "$manifest"

    if (( ${#TARGETS[@]} == 0 )); then
        echo "ERROR: no example targets were generated in ${manifest}" >&2
        return 1
    fi
}

configure_build() {
    local build_dir="$1"
    local build_type="$2"
    shift 2
    local extra_args=("$@")
    local abs_build_dir="${ROOT_DIR}/${build_dir}"
    local cmake_cmd=(cmake -S "$ROOT_DIR" -B "$abs_build_dir")

    if [[ -n "$PRELOAD_FILE" ]]; then
        if [[ ! -f "$PRELOAD_FILE" ]]; then
            echo "ERROR: preload file not found: ${PRELOAD_FILE}" >&2
            return 1
        fi
        cmake_cmd+=(-C "$PRELOAD_FILE")
    fi

    cmake_cmd+=(
        -DCROFT_BUILD_TESTS=OFF
        -DCMAKE_BUILD_TYPE="$build_type"
    )

    if (( ${#extra_args[@]} > 0 )); then
        cmake_cmd+=("${extra_args[@]}")
    fi

    run_cmd "${cmake_cmd[@]}"
}

build_targets() {
    local build_dir="$1"
    shift
    local abs_build_dir="${ROOT_DIR}/${build_dir}"
    run_cmd cmake --build "$abs_build_dir" --target "$@"
}

measure_targets() {
    local profile="$1"
    local build_dir="$2"
    local build_type="$3"
    local opt_level="$4"
    local lto="$5"
    local dead_strip="$6"
    local abs_build_dir="${ROOT_DIR}/${build_dir}"
    local target

    echo
    echo "=== Profile: ${profile} ==="
    echo "build_dir=${build_dir}"
    echo "build_type=${build_type}"
    echo "opt_level=${opt_level}"
    echo "lto=${lto}"
    echo "dead_strip=${dead_strip}"
    echo "targets=${TARGETS[*]}"

    for target in "${TARGETS[@]}"; do
        local binary_path="${abs_build_dir}/${target}"
        local binary_relpath="${build_dir}/${target}"
        local binary_bytes
        local stripped_copy
        local stripped_bytes

        if [[ ! -f "$binary_path" ]]; then
            echo "ERROR: target binary not found: ${binary_path}" >&2
            return 1
        fi

        binary_bytes="$(bytes_of "$binary_path")"
        stripped_copy="${RUNS_DIR}/${STAMP}--${SAFE_DESC}--$(sanitize "$target")--${profile}.stripped"
        cp "$binary_path" "$stripped_copy"
        if command -v strip >/dev/null 2>&1; then
            strip -S -x "$stripped_copy" >/dev/null 2>&1 || true
        fi
        stripped_bytes="$(bytes_of "$stripped_copy")"

        echo "-- ${target} --"
        echo "binary=${binary_relpath}"
        echo "binary_bytes=${binary_bytes}"
        echo "stripped_bytes=${stripped_bytes}"

        if command -v file >/dev/null 2>&1; then
            file "$binary_path" || true
        fi
        if command -v size >/dev/null 2>&1; then
            size "$binary_path" || true
        fi

        append_history_row \
            "$STAMP" \
            "$GIT_DESC" \
            "$target" \
            "$profile" \
            "$build_dir" \
            "$build_type" \
            "$opt_level" \
            "$lto" \
            "$dead_strip" \
            "$binary_relpath" \
            "$binary_bytes" \
            "$stripped_bytes" \
            "$(basename "$LOG_FILE")"
    done
}

build_and_measure_profile() {
    local profile="$1"
    local build_dir="$2"
    local build_type="$3"
    local opt_level="$4"
    local lto="$5"
    local dead_strip="$6"
    shift 6
    local extra_args=("$@")
    local abs_build_dir="${ROOT_DIR}/${build_dir}"

    configure_build "$build_dir" "$build_type" "${extra_args[@]}"
    if (( ${#TARGETS[@]} == 0 )); then
        load_targets_from_manifest "$abs_build_dir"
    fi
    build_targets "$build_dir" "${TARGETS[@]}"
    measure_targets "$profile" "$build_dir" "$build_type" "$opt_level" "$lto" "$dead_strip"
}

while (($#)); do
    case "$1" in
        --target)
            append_target "${2:?missing value for --target}"
            shift 2
            ;;
        --targets)
            IFS=',' read -r -a split_targets <<< "${2:?missing value for --targets}"
            for t in "${split_targets[@]}"; do
                append_target "$t"
            done
            shift 2
            ;;
        --preload)
            PRELOAD_FILE="${2:?missing value for --preload}"
            shift 2
            ;;
        --debug-dir)
            DEBUG_BUILD_DIR="${2:?missing value for --debug-dir}"
            shift 2
            ;;
        --opt-dir)
            OPT_BUILD_DIR="${2:?missing value for --opt-dir}"
            shift 2
            ;;
        --keep)
            KEEP_LOGS="${2:?missing value for --keep}"
            shift 2
            ;;
        --opt-level)
            OPT_LEVEL="${2:?missing value for --opt-level}"
            shift 2
            ;;
        --compare-debug)
            COMPARE_DEBUG=1
            shift
            ;;
        --help|-h)
            usage
            exit 0
            ;;
        *)
            echo "Unknown argument: $1" >&2
            usage >&2
            exit 2
            ;;
    esac
done

case "$OPT_LEVEL" in
    Os|Oz|O2|O3) ;;
    *)
        echo "Unsupported --opt-level: ${OPT_LEVEL} (expected Os|Oz|O2|O3)" >&2
        exit 2
        ;;
esac

if [[ "$PRELOAD_FILE" == "${ROOT_DIR}/local_deps/croft-deps.cmake" && ! -f "$PRELOAD_FILE" ]]; then
    PRELOAD_FILE=""
fi

mkdir -p "$RUNS_DIR"

GIT_DESC="$(git -C "$ROOT_DIR" describe --always --dirty --abbrev=7 2>/dev/null || echo no-git)"
STAMP="$(date +%Y-%B-%d-%H%M%S)"
SAFE_DESC="$(sanitize "$GIT_DESC")"
SAFE_TARGETS="$(sanitize "${TARGETS[*]:-all-examples}")"
LOG_FILE="${RUNS_DIR}/${STAMP}--${SAFE_DESC}--${SAFE_TARGETS}.log"

exec > >(tee -a "$LOG_FILE") 2>&1

echo "RUN_TIMESTAMP=${STAMP}"
echo "GIT_DESCRIBE=${GIT_DESC}"
echo "PRELOAD_FILE=${PRELOAD_FILE:-<none>}"
if (( ${#TARGETS[@]} > 0 )); then
    echo "SELECTED_TARGETS=${TARGETS[*]}"
else
    echo "SELECTED_TARGETS=<manifest>"
fi
echo "LOG_FILE=${LOG_FILE}"
echo

append_history_header_if_missing

status=0

OPT_FLAGS="-${OPT_LEVEL} -DNDEBUG -g0 -ffunction-sections -fdata-sections"
OPT_LDFLAGS="-Wl,-dead_strip -Wl,-x"

if build_and_measure_profile \
    "optimized" \
    "$OPT_BUILD_DIR" \
    "MinSizeRel" \
    "$OPT_LEVEL" \
    "on" \
    "on" \
    -DCMAKE_INTERPROCEDURAL_OPTIMIZATION=ON \
    -DCMAKE_C_FLAGS_MINSIZEREL="$OPT_FLAGS" \
    -DCMAKE_CXX_FLAGS_MINSIZEREL="$OPT_FLAGS" \
    -DCMAKE_OBJC_FLAGS_MINSIZEREL="$OPT_FLAGS" \
    -DCMAKE_OBJCXX_FLAGS_MINSIZEREL="$OPT_FLAGS" \
    -DCMAKE_EXE_LINKER_FLAGS_MINSIZEREL="$OPT_LDFLAGS"; then
    :
else
    status=$?
fi

if (( status == 0 && COMPARE_DEBUG == 1 )); then
    if build_and_measure_profile "debug" "$DEBUG_BUILD_DIR" "Debug" "debug" "off" "off"; then
        :
    else
        status=$?
    fi
fi

echo
echo "EXIT_STATUS=${status}"
echo "END_TIMESTAMP=$(date +%Y-%B-%d-%H%M%S)"

cleanup_old_logs "$KEEP_LOGS"
echo "History CSV: ${HISTORY_CSV}"
echo "Run log: ${LOG_FILE}"
exit "$status"
